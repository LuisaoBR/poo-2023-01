package Casa;

public class Garagem {
	
	private int qtdDeCarros;
	private double area;
	
	Garagem(){
		
	}

	public int getQtdDeCarros() {
		return qtdDeCarros;
	}

	public void setQtdDeCarros(int qtdDeCarros) {
		this.qtdDeCarros = qtdDeCarros;
	}

	public double getArea() {
		return area;
	}

	public void setArea(double area) {
		this.area = area;
	}
	
	

}
