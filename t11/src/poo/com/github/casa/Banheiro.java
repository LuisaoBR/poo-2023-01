package poo.com.github.casa;

public class Banheiro {

	private double tamanho;
	private boolean temChuveiro;
	private boolean temPrivada;

	Banheiro() {

	}

	public double getTamanho() {
		return tamanho;
	}

	public void setTamanho(double tamanho) {
		this.tamanho = tamanho;
	}

	public boolean isTemChuveiro() {
		return temChuveiro;
	}

	public void setTemChuveiro(boolean temChuveiro) {
		this.temChuveiro = temChuveiro;
	}

	public boolean isTemPrivada() {
		return temPrivada;
	}

	public void setTemPrivada(boolean temPrivada) {
		this.temPrivada = temPrivada;
	}

}
